    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        if(textCondicion.getText().equals("")){
            JOptionPane.showMessageDialog(this,"No se ingreso el codigo");
        } else{
            int i,j;
	    MDB administradorBD =  new MDB();
	    List<ArrayList<String>> datosObtenidos= new ArrayList<ArrayList<String>>();
	    ArrayList<String> renglonObtenido = new ArrayList<String>();
            DefaultListModel modelo = new DefaultListModel();
	    //String leyenda="";
            //consulta
            datosObtenidos = administradorBD.consultaAbierta("select p.idProducto,c.nombre,p.nombre,p.descripcion,p.cantidad,p.costo,p.precio,p.caducidad,p.ubicacion"
            + " from producto p left join categoria c on p.idCategoria = c.idCategoria where p.idProducto = '"+textCondicion.getText()+"'");
            //System.out.println(leyenda);
	    for(i=0;i<datosObtenidos.size();i++)
	    {
                renglonObtenido = datosObtenidos.get(i);
                for(j=0;j<renglonObtenido.size();j++){
                    modelo.addElement(renglonObtenido.get(j));
                }
            }
            lista.setModel(modelo);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

    private void textCondicionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textCondicionKeyTyped
        char validar = evt.getKeyChar();
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(this,"Ingresar solo numeros");
        }
    }//GEN-LAST:event_textCondicionKeyTyped

